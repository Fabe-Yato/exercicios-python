#3.Elabore um programa que receba três valores numéricos inteiros e apresente a soma dos quadrados dos três valores.

num1 = int(input("Digite um número: "))
num2 = int(input("Digite outro número: "))
num3 = int(input("Digite mais um número: "))

soma = (num1 ** 2) + (num2 ** 2) + (num3 ** 2)

print("\nO valor da soma do quadrado dos números é:", soma)